export const translateContent = async (request: TranslationRequest): Promise<TranslationResponse> => {
  const { content, target_language, source_language } = request;

  return new Promise((resolve) => {
    // Simulate network delay
    setTimeout(() => {
      let translated_content = content;

      // Simple translation based on predefined mappings
      if (target_language === 'ur' && source_language === 'en') {
        // English to Urdu translation
        // Split content into words and translate each word
        const words = content.split(/(\s+)/); // Split by whitespace but keep the spaces
        translated_content = words.map(word => {
          // Check if the word exists in the map (case-insensitive)
          const trimmedWord = word.trim();
          if (trimmedWord && englishToUrduMap[trimmedWord]) {
            return englishToUrduMap[trimmedWord];
          } else if (trimmedWord && englishToUrduMap[trimmedWord.toLowerCase()]) {
            // Try lowercase version
            return englishToUrduMap[trimmedWord.toLowerCase()];
          }
          // If not found, return the original word
          return word;
        }).join('');
      } else if (target_language === 'en' && source_language === 'ur') {
        // Urdu to English translation (reverse mapping)
        const urduToEnglishMap: Record<string, string> = {};
        Object.entries(englishToUrduMap).forEach(([en, ur]) => {
          urduToEnglishMap[ur] = en;
        });

        const words = content.split(/(\s+)/); // Split by whitespace but keep the spaces
        translated_content = words.map(word => {
          const trimmedWord = word.trim();
          if (trimmedWord && urduToEnglishMap[trimmedWord]) {
            return urduToEnglishMap[trimmedWord];
          }
          return word;
        }).join('');
      }

      resolve({
        translated_content,
        source_language,
        target_language,
      });
    }, 300); // Simulate 300ms network delay
  });
};